test('products table image column returns accessible url for stored image path', function () {
    $staff = User::factory()->staff()->create();
    Storage::fake('public');

    $product = Product::factory()->create([
        'images' => ['products/test-frame.jpg'],
    ]);

    Storage::disk('public')->put('products/test-frame.jpg', 'fake-image');

    $this->actingAs($staff);

    // The image URL generated by Storage::disk should be a full URL
    $url = Storage::disk('public')->url('products/test-frame.jpg');
    expect($url)->toContain('products/test-frame.jpg');

    // The API should return the full URL for images, not raw paths
    $customer = User::factory()->customer()->create();
    $this->actingAs($customer, 'sanctum')
        ->getJson("/api/products/{$product->id}")
        ->assertSuccessful()
        ->assertJsonPath('data.images.0', 'products/test-frame.jpg'); // raw path — Android builds URL
});
